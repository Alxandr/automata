const icon = (name) => ({
  '&:before': {
    'content': `"${iconNames[name]}"`
  }
});

const iconStyles =
  Object.keys(iconNames)
    .reduce((styles, name) => {
      const style = icon(name);
      return { ...styles, [name]: style };
    }, {});

const styleSheet = createStyleSheet('Icon', () => ({
  icon: {
    'display': 'inline-block',
    'font': 'normal normal normal 1.2em/1 metro',
    'font-size': 'inherit',
    'text-rendering': 'auto',
    '-webkit-font-smoothing': 'antialiased',
    'vertical-align': 'middle'
  },

  ...iconStyles
}));

const Icon =
  composeComponent(
    setDisplayName('Icon'),
    withStyleSheet(styleSheet),
    withClasses(({ type }) => {
      warning(
        iconNames.hasOwnProperty(type),
        `Icon with type ${type} not found`
      );

      return ['icon', type];
    }),
    addClass(iconClassName),
    removeInvalid(),
    ({ className }) => <span className={className} />
  );

Icon.propTypes = {
  type: PropTypes.string.isRequired
};

Icon.className = iconClassName;

export default Icon;
